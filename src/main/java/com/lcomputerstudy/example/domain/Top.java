package com.lcomputerstudy.example.domain;

public class Top {
	private String i_idx;
	private String top_idx;
	private String name;
	private String top_id;
	
	public String getI_idx() {
		return i_idx;
	}
	public void setI_idx(String i_idx) {
		this.i_idx = i_idx;
	}
	public String getTop_idx() {
		return top_idx;
	}
	public void setTop_idx(String top_idx) {
		this.top_idx = top_idx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTop_id() {
		return top_id;
	}
	public void setTop_id(String top_id) {
		this.top_id = top_id;
	}

	

}
